    private void forgotPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_forgotPasswordMouseClicked
        //FUNCTION TO RESET PASSWORD USING JAVA MAIL API
        /*
        Display a window confirming user's Id.
        */
        JFrame f;
        f = new JFrame();
        String id = JOptionPane.showInputDialog(f, "Enter your USERID to receive\na password reset through email.");
        String receiverEmail = "";
        /*
        Extracting the user's mail id registered in the database
        */
        try{
           
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/VoteApp","root","root");
            Statement st = con.createStatement();
            String sql = "SELECT Email FROM Users WHERE Id = " + id + " ;";
            ResultSet rs = st.executeQuery(sql);
            rs.next();
            receiverEmail = rs.getString("Email");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
        /*
        Generating a  new random unique Password using the self built UniquePassword class
        */
        UniquePassword generator = new UniquePassword();
        String newPassword = generator.generatePassowrd();
        /*
        Assigning value to senderEmail and password
        */
        final String senderEmail = "yuvrajjoshi31@gmail.com";
        final String password = "123*yuvraj*123";
        /*
        Using JavaMail API to send email using Gmail SMTP server
        */
        /**
	   Outgoing Mail (SMTP) Server
	   requires TLS or SSL: smtp.gmail.com (use authentication)
	   Use Authentication: Yes
	   Port for TLS/STARTTLS: 587 //default port
	 */
        Properties props = new Properties();
        props.put("mail.smtp.auth", "true");    //enable authentication
        props.put("mail.smtp.starttls.enable", "true"); //enable STARTLLS
        props.put("mail.smtp.host", "smtp.gmail.com"); //SMTP host: protocol to send mails over TCP/IP
        props.put("mail.smtp.port", "587"); //TLS Port for security
        
         /*
        Creating a Session object which stores the host name, username, password etc.
        */
        Session session = Session.getInstance(props,
                new javax.mail.Authenticator() {
                    protected PasswordAuthentication getPasswordAuthentication() {
                        return new PasswordAuthentication(senderEmail, password);
                    }
                });
        //BODY OF EMAIL
        try {
            /*
            Creating message
            */
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(senderEmail));
            message.setRecipients(Message.RecipientType.TO,
                    InternetAddress.parse(receiverEmail));
            message.setSubject("Vote App Password Reset");
            message.setText("Your new password is " +
                    newPassword);
            /*
            Sending the message
            */
            Transport.send(message);
 
        } catch (MessagingException e) {
            throw new RuntimeException(e);
        }
        /*
        Updating the database with the new generated password.
        */
        try{
           
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/VoteApp","root","root");
            Statement st = con.createStatement();
            String sql = "UPDATE Users SET Password = '" + newPassword + "' WHERE Id = " + id + ";";
            int rs = st.executeUpdate(sql);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_forgotPasswordMouseClicked

