    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        /*
        Make sure Username and Password are not empty
        */
        if(IdField.getText().equals(""))
            UserEmpty.setVisible(true);

        else if(PswrdField.getText().equals(""))
            PswrdEmpty.setVisible(true);

        else{
        try{
            /*
            Comparing and matching the entered username,password with the one registered in the database
            */
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/VoteApp","root","root");
            Statement st = con.createStatement();
            String sql = "SELECT Password, Type from Users where Id = " + IdField.getText() + " ;";
            ResultSet rs = st.executeQuery(sql);
            rs.next();
            String original_pswrd = rs.getString("Password");
            String type = rs.getString("Type");
            String pswrd = new String(PswrdField.getPassword());
            
            if(original_pswrd.equals(pswrd))
            {
                String id[] = new String[1];
                id[0] = IdField.getText();
                if(type.equals("A"))
                {
                    //If user has Admin priveleges then open AdminPage
                    AdminPage.main(id);
                    this.dispose();
                }
                else
                {
                    //If user is a student then open student's page.
                    StudentPage.main(id);
                    this.dispose();
                }
            }
            else
                Invalid.setVisible(true);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Username does not exist.");
        }
      }
        
    }//GEN-LAST:event_signInActionPerformed

