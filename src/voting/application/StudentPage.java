//FUNCTION TO CHECK IF STUDENT HAS ALREADY VOTED FOR THE SELECTED SESSION
private boolean checkIfAttempted(String check){
    boolean b = false;
    try{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/VoteApp","root","root");
                Statement st = con.createStatement();
                String query = "SELECT " + check + " FROM Attempted WHERE StudentID = " + studentID + ";";
                ResultSet r = st.executeQuery(query);
                r.next();
                if(r.getString(check).equals("Yes"))
                    b = true;
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);
            }
    return b;
}    
    
    private void loadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadActionPerformed
        String[] sessionAndStudentID = new String[2];
        sessionAndStudentID[0] = sessionField.getText();
        sessionAndStudentID[1] = studentID;
        //RECREATING SESSION STRING TO MATCH WITH TABLE'S COLUMN NAME (S+[SESSIONID])
        String check = "S" + sessionAndStudentID[0];
        
        boolean isAttempted = checkIfAttempted(check);
        if(isAttempted == true)
            JOptionPane.showMessageDialog(null, "You have already submitted votes for this session!");
        else{
            //FILLING UP SESSION DETAILS
            try{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/VoteApp","root","root");
                Statement st = con.createStatement();
                String query1 = "SELECT Category FROM Session_details WHERE SessionID = " + sessionAndStudentID[0] + ";";
                ResultSet rs1 = st.executeQuery(query1);
                rs1.next();
                topicLabel.setText(rs1.getString("Category"));
                String query3 = "SELECT COUNT(*) FROM type_descriptive WHERE sessionID = " + sessionAndStudentID[0] + ";";
                String query4 = "SELECT COUNT(*) FROM type_mcq WHERE sessionID = " + sessionAndStudentID[0] + ";";
                String query5 = "SELECT COUNT(*) FROM type_truefalse WHERE sessionID = " + sessionAndStudentID[0] + ";";
                ResultSet rs3 = st.executeQuery(query3);
                rs3.next();
                descCount.setText(rs3.getString("Count(*)"));
                ResultSet rs4 = st.executeQuery(query4);
                rs4.next();
                mcqCount.setText(rs4.getString("Count(*)"));
                ResultSet rs5 = st.executeQuery(query5);
                rs5.next();          
                tfCount.setText(rs5.getString("Count(*)"));
                totalQuesLabel.setText(""+(Integer.parseInt(descCount.getText()) + Integer.parseInt(mcqCount.getText()) + Integer.parseInt(tfCount.getText())));
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null,e);
            }
            //OPEN QUESTIONS TABLE
            VoteQuestions.main(sessionAndStudentID);
        }
        
    }//GEN-LAST:event_loadActionPerformed

