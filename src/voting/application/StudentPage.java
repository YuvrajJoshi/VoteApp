        topicList.addItemListener(new ItemListener(){
            public void itemStateChanged(ItemEvent event){
                if(event.getStateChange() == ItemEvent.SELECTED){
                    Object item = event.getItem();
                    String selectedItem = item.toString();
                    try{
                        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/VoteApp","root","root");
                        Statement st = con.createStatement();
                        String query1 = "SELECT SessionID FROM Session_Details WHERE Category = '" + selectedItem + "';";
                        ResultSet rs1 = st.executeQuery(query1);
                        while(rs1.next())
                        {
                            String sessionID = rs1.getString("SessionID");
                            sessionList.addItem(sessionID);
                        }
                    }
                    catch(SQLException e){
                        JOptionPane.showMessageDialog(null,e);
                    }
                }
                else if(event.getStateChange() == ItemEvent.DESELECTED){
                    sessionList.removeAllItems();
                }
            }
        });
        sessionList.addItemListener(new ItemListener(){
            public void itemStateChanged(ItemEvent event){
                if(event.getStateChange() == ItemEvent.SELECTED){
                    Object item = event.getItem();
                    String selectedItem = item.toString();
                    sessionField.setText(selectedItem);
                }
            }
        });

